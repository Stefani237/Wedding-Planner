public class WeddingDetailsClass {
    private String mGroomName;
    private String mBrideName;
    private String mWeddingHall;
    private String mAddress;
    private String mDate;
    private String mTime;

    public String getmGroomName() {
        return mGroomName;
    }

    public void setmGroomName(String mGroomName) {
        this.mGroomName = mGroomName;
    }

    public String getmBrideName() {
        return mBrideName;
    }

    public void setmBrideName(String mBrideName) {
        this.mBrideName = mBrideName;
    }

    public String getmWeddingHall() {
        return mWeddingHall;
    }

    public void setmWeddingHall(String mWeddingHall) {
        this.mWeddingHall = mWeddingHall;
    }

    public String getmAddress() {
        return mAddress;
    }

    public void setmAddress(String mAddress) {
        this.mAddress = mAddress;
    }

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmTime() {
        return mTime;
    }

    public void setmTime(String mTime) {
        this.mTime = mTime;
    }
}
